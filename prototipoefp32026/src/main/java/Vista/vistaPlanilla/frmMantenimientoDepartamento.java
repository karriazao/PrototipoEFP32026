    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        DepartamentoDAO dao = new DepartamentoDAO();

        clsBitacora bitacora = new clsBitacora();

        int id = Integer.parseInt(txtBuscar.getText());

        clsDepartamento departamento
                = dao.obtenerDepartamentoPorId(id, bitacora);

        if (departamento != null) {

            txtCodigo.setText(
                    String.valueOf(departamento.getDepcodigo()));

            txtNombre.setText(
                    departamento.getDepnombre());

            if (departamento.getDepestado() == 1) {

                chkEstado.setSelected(true);

            } else {

                chkEstado.setSelected(false);
            }

        } else {

            JOptionPane.showMessageDialog(null,
                    "Departamento no encontrado");
        }
    
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        clsDepartamento departamento = new clsDepartamento();

        departamento.setDepcodigo(
                Integer.parseInt(txtCodigo.getText()));

        departamento.setDepnombre(
                txtNombre.getText());

        if (chkEstado.isSelected()) {

            departamento.setDepestado(1);

        } else {

            departamento.setDepestado(0);
        }

        DepartamentoDAO dao = new DepartamentoDAO();

        clsBitacora bitacora = new clsBitacora();

        int resultado
                = dao.actualizarDepartamento(departamento, bitacora);

        if (resultado > 0) {

            JOptionPane.showMessageDialog(null,
                    "Registro modificado");

            cargarTabla();

            txtCodigo.setText("");
            txtNombre.setText("");
            txtBuscar.setText("");

            chkEstado.setSelected(false);

        } else {

            JOptionPane.showMessageDialog(null,
                    "Error al modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

