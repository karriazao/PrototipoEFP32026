    private void calcularTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularTotalActionPerformed

      
    try {
        // Obtenemos los valores de los cuadros de texto (Imagen 0aca96)
        double cantidad = Double.parseDouble(cantidadProducto.getText());
        // Nota: Asegúrate de tener una variable para Precio Unitario, ej: txtPrecio
        double precio = Double.parseDouble(productoId.getText()); 
        
        double subtotal = cantidad * precio;
        double impuesto = subtotal * 0.12; // Ejemplo IVA 12%
        double total = subtotal + impuesto;

        // Mostramos los resultados (Imagen 23:11:34)
        subtotalPrevia.setText(String.valueOf(subtotal));
        totalPrevia.setText(String.valueOf(total));
        
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor ingrese solo números en cantidad y precio.");
    }


        // TODO add your handling code here:
    }//GEN-LAST:event_calcularTotalActionPerformed

