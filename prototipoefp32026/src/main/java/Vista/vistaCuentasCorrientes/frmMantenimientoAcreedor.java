    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Nit");
        modelo.addColumn("Cuenta Bancaria");
        modelo.addColumn("Estado");
        clsAcreedor Acreedor = new clsAcreedor();
   
        List<clsAcreedor> listaAcreedor = Acreedor.getListadoAcreedores();
        tablaAcreedores.setModel(modelo);
        String[] dato = new String[5];
        for (int i = 0; i < listaAcreedor.size(); i++) {
        dato[0] = Integer.toString(listaAcreedor.get(i).getAcreCodigo());
        dato[1] = listaAcreedor.get(i).getAcreNombre();
        dato[2] = listaAcreedor.get(i).getAcreNit();
        dato[3] = listaAcreedor.get(i).getAcreCuentaBancaria();
        dato[4] = listaAcreedor.get(i).getAcreEstado();
        modelo.addRow(dato);
    }
}  
              
    

