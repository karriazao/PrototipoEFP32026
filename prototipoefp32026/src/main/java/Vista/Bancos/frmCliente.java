    private void actualizaarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizaarActionPerformed
    if (txtbuscado.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Seleccione un cliente de la tabla para actualizar.",
            "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!camposCompletos()) return;

    try {
        Controlador.Bancos.clsCliente cliente = getClienteDeFormulario();
        cliente.setClid(Integer.parseInt(txtbuscado.getText().trim()));
        clientesDAO.update(cliente);
        javax.swing.JOptionPane.showMessageDialog(this,
            "Cliente actualizado correctamente.",
            "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        frmBitacoraBancaria.registrarBitacora(
    "UPDATE",
    "Cliente",
    Integer.parseInt(txtbuscado.getText().trim()),
    null,
    "Nombre: " + jTextField1.getText().trim() +
    " | NIT: " + jTextField2.getText().trim() +
    " | Teléfono: " + jTextField3.getText().trim() +
    " | Estado: " + jTextField7.getText().trim() +
    " | Dirección: " + jTextField6.getText().trim() +
    " | Correo: " + jTextField4.getText().trim(),
    "Cliente actualizado"
);
        cargarTabla();
        limpiarCampos();
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "El ID debe ser un número válido.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al actualizar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_actualizaarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
       if (txtbuscado.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Ingrese un ID para buscar.",
            "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int id = Integer.parseInt(txtbuscado.getText().trim());
        Controlador.Bancos.clsCliente cliente = clientesDAO.query(id);

        if (cliente != null) {
            jTextField1.setText(cliente.getClinombre());
            jTextField2.setText(cliente.getClinit());
            jTextField3.setText(cliente.getClitelefono());
            jTextField7.setText(cliente.getCliestado());
            jTextField6.setText(cliente.getClidireccion());
            jTextField4.setText(cliente.getClicorreo());
            frmBitacoraBancaria.registrarBitacora(
    "SELECT",
    "Cliente",
    Integer.parseInt(txtbuscado.getText().trim()),
    null,
    null,
    "Consulta de cliente por ID"
);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró un cliente con ID: " + id,
                "Sin resultados", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        }
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "El ID debe ser un número válido.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al buscar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_buscarActionPerformed

    private void reporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteActionPerformed
     Connection conn = null;
    try {
        conn = Modelo.Conexion.getConnection();
        
        // Ruta corregida — faltaba el separador entre getAbsolutePath() y src
        String ruta = new java.io.File("").getAbsolutePath() 
                    + "\\src\\main\\java\\Reportes\\Bancos\\Cliente.jrxml";
        
        System.out.println("Buscando reporte en: " + ruta); // Para verificar en consola
        
        java.io.File archivo = new java.io.File(ruta);
        if (!archivo.exists()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró el archivo del reporte en:\n" + ruta,
                "Archivo no encontrado", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        java.util.Map<String, Object> parametros = new java.util.HashMap<>();
        
        net.sf.jasperreports.engine.JasperReport reporte =
            net.sf.jasperreports.engine.JasperCompileManager.compileReport(ruta);
        
        net.sf.jasperreports.engine.JasperPrint print =
          net.sf.jasperreports.engine.JasperFillManager.fillReport(reporte, parametros, conn);
        
        net.sf.jasperreports.engine.JasperExportManager.exportReportToPdfFile(print,"reporte.pdf");
        net.sf.jasperreports.swing.JRViewer viewer = new net.sf.jasperreports.swing.JRViewer(print);
        
        javax.swing.JFrame frame = new javax.swing.JFrame("Reporte de Clientes");
        frame.setSize(800, 600);
        frame.setLocationRelativeTo(null);
        frame.setDefaultCloseOperation(javax.swing.JFrame.DISPOSE_ON_CLOSE);
        frame.add(viewer);
        frame.setVisible(true);
        
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al generar el reporte:\n" + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } finally {
        if (conn != null) {
            try { conn.close(); } catch (Exception ex) { ex.printStackTrace(); }
        }
    }
    }//GEN-LAST:event_reporteActionPerformed

