// ── Llena la tabla con todos los clientes ────────────────────
private void cargarTabla() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new String[]{"ID", "Nombre", "NIT", "Teléfono", "Estado", "Dirección", "Correo"}, 0
    ) {
        @Override
        public boolean isCellEditable(int r, int c) { return false; }
    };

    for (Controlador.Bancos.clsCliente c : clientesDAO.listar()) {
        modelo.addRow(new Object[]{
            c.getClid(),
            c.getClinombre(),
            c.getClinit(),
            c.getClitelefono(),
            c.getCliestado(),
            c.getClidireccion(),
            c.getClicorreo()
            // El ID lo genera MySQL con AUTO_INCREMENT, no lo escribe el usuario
        });
    }
    jTable2.setModel(modelo);
}

// ── Clic en fila → llena campos automáticamente ──────────────
private void configurarSeleccionTabla() {
    jTable2.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && jTable2.getSelectedRow() != -1) {
            int fila = jTable2.getSelectedRow();
            txtbuscado.setText(jTable2.getValueAt(fila, 0).toString()); // ID automático
            jTextField1.setText(jTable2.getValueAt(fila, 1).toString()); // Nombre
            jTextField2.setText(jTable2.getValueAt(fila, 2).toString()); // NIT
            jTextField3.setText(jTable2.getValueAt(fila, 3).toString()); // Teléfono
            jTextField7.setText(jTable2.getValueAt(fila, 4).toString()); // Estado
            jTextField6.setText(jTable2.getValueAt(fila, 5).toString()); // Dirección
            jTextField4.setText(jTable2.getValueAt(fila, 6).toString()); // Correo
        }
    });
}

// ── Limpia todos los campos ──────────────────────────────────
private void limpiarCampos() {
    txtbuscado.setText("");
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTextField6.setText("");
    jTextField7.setText("");
    jTable2.clearSelection();
}

// ── Valida que los campos no estén vacíos ───────────────────
private boolean camposCompletos() {
    if (jTextField1.getText().trim().isEmpty() ||
        jTextField2.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() ||
        jTextField7.getText().trim().isEmpty() ||
        jTextField6.getText().trim().isEmpty() ||
        jTextField4.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Complete todos los campos: Nombre, NIT, Teléfono, Estado, Dirección y Correo.",
            "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}

// ── Crea el objeto clsCliente desde los campos del formulario ─
private Controlador.Bancos.clsCliente getClienteDeFormulario() {
    Controlador.Bancos.clsCliente c = new Controlador.Bancos.clsCliente();
    c.setClinombre(jTextField1.getText().trim());   // Nombre
    c.setClinit(jTextField2.getText().trim());       // NIT
    c.setClitelefono(jTextField3.getText().trim());  // Teléfono
    c.setCliestado(jTextField7.getText().trim());    // Estado
    c.setClidireccion(jTextField6.getText().trim()); // Dirección
    c.setClicorreo(jTextField4.getText().trim());    // Correo
    // Clid no se setea → MySQL lo genera automáticamente con AUTO_INCREMENT
    return c;
}
