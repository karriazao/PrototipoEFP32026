    private void reporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reporteActionPerformed
       Connection conn = null;
    try {
        conn = Modelo.Conexion.getConnection();
        
        // Ruta corregida — faltaba el separador entre getAbsolutePath() y src
        String ruta = new java.io.File("").getAbsolutePath() 
                    + "\\src\\main\\java\\Reportes\\Bancos\\Banco.jrxml";
        
        System.out.println("Buscando reporte en: " + ruta); // Para verificar en consola
        
        java.io.File archivo = new java.io.File(ruta);
        if (!archivo.exists()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró el archivo del reporte en:\n" + ruta,
                "Archivo no encontrado", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        java.util.Map<String, Object> parametros = new java.util.HashMap<>();
        
        net.sf.jasperreports.engine.JasperReport reporte =
           net.sf.jasperreports.engine.JasperCompileManager.compileReport(ruta);
        
        net.sf.jasperreports.engine.JasperPrint print =
           net.sf.jasperreports.engine.JasperFillManager.fillReport(reporte, parametros, conn);
        
        net.sf.jasperreports.engine.JasperExportManager.exportReportToPdfFile(print,"reporte.pdf");
        net.sf.jasperreports.swing.JRViewer viewer = new net.sf.jasperreports.swing.JRViewer(print);
        
        javax.swing.JFrame frame = new javax.swing.JFrame("Reporte de Clientes");
        frame.setSize(800, 600);
        frame.setLocationRelativeTo(null);
        frame.setDefaultCloseOperation(javax.swing.JFrame.DISPOSE_ON_CLOSE);
        frame.add(viewer);
        frame.setVisible(true);
        
    } catch (Exception e) {
        e.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al generar el reporte:\n" + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } finally {
        if (conn != null) {
            try { conn.close(); } catch (Exception ex) { ex.printStackTrace(); }
        }
    }
    }//GEN-LAST:event_reporteActionPerformed
 // ── Llena la tabla con todos los bancos ──────────────────────
private void cargarTabla() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new String[]{"ID", "Nombre", "Dirección", "Teléfono", "Correo", "Fecha"}, 0
    ) {
        @Override
        public boolean isCellEditable(int r, int c) { return false; }
    };

    for (Controlador.Bancos.clsBanco b : bancoDAO.listar()) {
        modelo.addRow(new Object[]{
            b.getBanid(),
            b.getBannombre(),
            b.getBandireccion(),
            b.getBantelefono(),
            b.getBancorreo(),
            b.getBanfecharegistro()  // La fecha viene de la BD, el usuario no la escribe
        });
    }
    jTable2.setModel(modelo);
}

// ── Clic en fila → llena campos automáticamente ──────────────
private void configurarSeleccionTabla() {
    jTable2.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && jTable2.getSelectedRow() != -1) {
            int fila = jTable2.getSelectedRow();
            txtbuscado.setText(jTable2.getValueAt(fila, 0).toString()); // ID automático
            jTextField1.setText(jTable2.getValueAt(fila, 1).toString());
            jTextField2.setText(jTable2.getValueAt(fila, 2).toString());
            jTextField3.setText(jTable2.getValueAt(fila, 3).toString());
            jTextField4.setText(jTable2.getValueAt(fila, 4).toString());
        }
    });
}

// ── Limpia todos los campos ──────────────────────────────────
private void limpiarCampos() {
    txtbuscado.setText("");
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField3.setText("");
    jTextField4.setText("");
    jTable2.clearSelection();
}

// ── Valida que los campos no estén vacíos ────────────────────
private boolean camposCompletos() {
    if (jTextField1.getText().trim().isEmpty() ||
        jTextField2.getText().trim().isEmpty() ||
        jTextField3.getText().trim().isEmpty() ||
        jTextField4.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Complete todos los campos: Nombre, Dirección, Teléfono y Correo.",
            "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}

// ── Crea el objeto clsBanco desde los campos del formulario ──
private Controlador.Bancos.clsBanco getBancoDeFormulario() {
    Controlador.Bancos.clsBanco b = new Controlador.Bancos.clsBanco();
    b.setBannombre(jTextField1.getText().trim());
    b.setBandireccion(jTextField2.getText().trim());
    b.setBantelefono(jTextField3.getText().trim());
    b.setBancorreo(jTextField4.getText().trim());
    // No se setea Banid ni Banfecharegistro → los genera MySQL con AUTO_INCREMENT y DEFAULT NOW()
    return b;
}
