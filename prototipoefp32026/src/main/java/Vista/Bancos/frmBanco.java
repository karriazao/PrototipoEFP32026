    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
if (!camposCompletos()) return;

    try {
        Controlador.Bancos.clsBanco banco = getBancoDeFormulario();
        bancoDAO.insert(banco); // El ID y la fecha los genera la BD automáticamente
        javax.swing.JOptionPane.showMessageDialog(this,
            "Banco registrado correctamente.",
            "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        // ← AQUÍ se registra en la bitácora
        frmBitacoraBancaria.registrarBitacora(
            "INSERT", "Banco", null, null,
            "Nombre: " + jTextField1.getText().trim() +
            " | Dirección: " + jTextField2.getText().trim() +
            " | Teléfono: " + jTextField3.getText().trim() +
            " | Correo: " + jTextField4.getText().trim(),
            "Banco registrado"
        );
        cargarTabla();
        limpiarCampos();
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al agregar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_agregarActionPerformed

    private void eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarActionPerformed
        if (txtbuscado.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Seleccione un banco de la tabla o ingrese un ID.",
            "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
        "¿Está seguro de que desea eliminar este banco?",
        "Confirmar eliminación", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm != javax.swing.JOptionPane.YES_OPTION) return;

    try {
        int id = Integer.parseInt(txtbuscado.getText().trim());
        bancoDAO.delete(id);
        javax.swing.JOptionPane.showMessageDialog(this,
            "Banco eliminado correctamente.",
            "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        frmBitacoraBancaria.registrarBitacora(
    "DELETE",
    "Banco",
    Integer.parseInt(txtbuscado.getText().trim()),
    "Nombre: " + jTextField1.getText().trim(),
    null,
    "Banco eliminado"
);
        cargarTabla();
        limpiarCampos();
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "El ID debe ser un número válido.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al eliminar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_eliminarActionPerformed

    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed
          if (txtbuscado.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Ingrese un ID para buscar.",
            "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        int id = Integer.parseInt(txtbuscado.getText().trim());
        Controlador.Bancos.clsBanco banco = bancoDAO.query(id);

        if (banco != null) {
            jTextField1.setText(banco.getBannombre());
            jTextField2.setText(banco.getBandireccion());
            jTextField3.setText(banco.getBantelefono());
            jTextField4.setText(banco.getBancorreo());
            frmBitacoraBancaria.registrarBitacora(
    "SELECT",
    "Banco",
    Integer.parseInt(txtbuscado.getText().trim()),
    null,
    null,
    "Consulta de banco por ID"
);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se encontró un banco con ID: " + id,
                "Sin resultados", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
        }
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "El ID debe ser un número válido.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al buscar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_buscarActionPerformed

