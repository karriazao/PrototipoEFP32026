// ── Llena la tabla con todos los estados de conciliación ────────────────────
private void cargarTabla() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new String[]{"ID", "Fecha", "Saldo Sistema", "Saldo Banco", "Diferencia", "ID Cuenta", "ID Estado"}, 0
    ) {
        @Override
        public boolean isCellEditable(int r, int c) { return false; }
    };
    for (clsConciliacionBancaria cb : dao.listar()) {
        modelo.addRow(new Object[]{
            cb.getConbid(),
            cb.getConbfecha(),
            cb.getConbsaldosistema(),
            cb.getConbsaldobanco(),
            cb.getConbdiferencia(),
            cb.getCBANid()
        });
    }
    jTable1.setModel(modelo);
}

// ── Limpia todos los campos ──────────────────────────────────
private void limpiarCampos() {
    txtid.setText("");
    txtFecha.setText("");
    jTable1.clearSelection();
}

// ── Valida que los campos no estén vacíos ───────────────────
private boolean camposCompletos() {
    if (txtFecha.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Complete todos los campos: Nombre Estado.",
            "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}
private void configurarSeleccionTabla() {
    jTable1.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
            int fila = jTable1.getSelectedRow();
            txtid.setText(jTable1.getValueAt(fila, 0).toString());      // ID
            txtFecha.setText(jTable1.getValueAt(fila, 1).toString()); // Nombre Estado
        }
    });
}

// ── Crea el objeto clsConciliacionBancaria desde los campos del formulario ─
private clsConciliacionBancaria getConciliacionBancariaDeFormulario() {

    // Parsear fecha desde el JTextField
    java.util.Date fecha = null;
    try {
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        sdf.setLenient(false);
        fecha = sdf.parse(txtFecha.getText().trim());
    } catch (java.text.ParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Formato de fecha incorrecto. Use dd/MM/yyyy",
            "Error de fecha", javax.swing.JOptionPane.ERROR_MESSAGE);
        return null;
    }

    clsConciliacionBancaria cb = new clsConciliacionBancaria(
    0,
    fecha,
    Double.parseDouble(jTextField3.getText().trim()),  // Saldo Sistema
    Double.parseDouble(jTextField4.getText().trim()),  // Saldo Banco
    Double.parseDouble(jTextField5.getText().trim()),  // Diferencia
    Integer.parseInt(jTextField2.getText().trim()),    // ← ID Cuenta (número)
    cmbEstado.getSelectedIndex() + 1
);
    return cb;
} 

